#include <stdio.h>

int main(){

	//	Increment and Decrement Operators / Artýrma ve Azaltma Operatörleri
	/*
		++	1 arttýrýr				
		--	1 azaltýr
		
				Önemli
				Arttýrma ve azaltma operatörleri deðiþkenin önünde ya da arkasýnda olma durumuna göre deðiþik yapýda çalýþýr!!
				prefix and postfix
	*/
	
	int sayi = 5; 
	sayi++;
	sayi++;
	sayi--;
	//printf("Sayinin yeni degeri : %d", sayi);
	
	
	
	
	
	int sayi1 = 1;
	printf("Sayi1 in yeni degeri: %d", sayi1++);

	printf("\nSayi1 suanki deger : %d", sayi1);

	int sayi2 = 1;
	printf("\nSayi2 nin yeni degeri: %d", ++sayi2);
	
	
	
	return 0;
}





